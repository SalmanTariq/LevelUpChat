}
const navigation = [
    {name: 'Room List', href: '#', icon: HomeIcon, current: false, isItOn: "off", atClick: '"showRooms"'},
    {name: 'My Webcam', href: '#', icon: FireIcon, current: true, isItOn: "on", atClick: '@click="showUserSettings"'},
    {
        name: 'Private Chat',
        href: '#',
        icon: BookmarkAltIcon,
        current: false,
        isItOn: "off",
        atClick: '@click="showUserSettings"'
    },
    {name: 'All Sounds', href: '#', icon: InboxIcon, current: true, isItOn: "on", atClick: '@click="showUserSettings"'},
    {
        name: 'Support',
        href: '#',
        icon: InboxIcon,
        current: false,
        isItOn: "blank",
        atClick: '@click="showUserSettings"'
