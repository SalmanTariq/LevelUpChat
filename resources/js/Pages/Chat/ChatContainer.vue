]

export default {
    components: {
        InputBox,
        MessageContainer,
        UserList,
        Dialog,
        DialogOverlay,
        TransitionChild,
        TransitionRoot,
        MenuIcon,
        XIcon,
        RoomTabs,
        UserSettingsSlideout,
        UserProfileSlideout,
        MyWebcamSlideout,
        RoomsModal,
    },
    data: function () {
        return {
            chatRooms: [],
            currentRoom: [],
            messages: [],
            showUserSettingsSlideout: false,
            showMyWebcamSlideout: false,
            showUserProfileSlideout: false,
            showRoomsModal: false,
            users: []
        }
    },
    watch: {
        currentRoom() {
            this.connect();
        }
    },
    methods: {
        connect() {
            if (this.currentRoom?.id) {
                let vm = this;
                this.getMessages();
                Echo.join("chat." + this.currentRoom.id)
                    .here(users => this.users = users)
                    .joining(user => this.users.push(user))
                    .leaving(user => (this.users = this.users.filter(({id}) => (id !== user.id))))
                    .listen('NewChatMessage', e => {
                        vm.getMessages();
                        console.log('et');
                    });
            }
        },
        showUserSettings() {
            this.showUserSettingsSlideout = !this.showUserSettingsSlideout;
        },
        showMyWebcamS() {
            this.showMyWebcamSlideout = !this.showMyWebcamSlideout;
        },
        showUserProfile() {
            this.showUserProfileSlideout = !this.showUserProfileSlideout;
        },
        showRooms() {
            this.showRoomsModal = !this.showRoomsModal;
        },
        getRooms() {
            axios.get('/chat/rooms')
                .then(response => {
                    this.chatRooms = response.data;
                    this.setRoom(response.data[0]);
                })
                .catch(error => {
                    console.log(error);
                })
        },
        setRoom(room) {
            this.currentRoom = room;

        },
        getMessages() {
            axios.get('/chat/room/' + this.currentRoom.id + '/messages')
                .then(response => {
                    this.messages = response.data;
                })
                .catch(error => {
                    console.log(error);
                })
        },
    },
    created() {
        this.getRooms();
    },
    setup() {
        const mobileMenuOpen = ref(false)
        // onBeforeUnmount(this.unSubscribeFromChat)
        return {
            user,
            navigation,
            mobileMenuOpen,
        }
    },
}
</script>
